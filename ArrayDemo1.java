public class ArrayDemo1
{
    public static void main(String[] args)
    {

        int[] Array1 = {1,2,3};
        int[] Array2 = {4,5,6};


        System.out.println("+ : " + (Array1[0] + Array2[0]) + "," + (Array1[1] + Array2[1]) + "," + (Array1[2] + Array2[2]));
        System.out.println("* : " + (Array1[0] * Array2[0]) + "," + (Array1[1] * Array2[1]) + "," + (Array1[2] * Array2[2]));
    }


}
